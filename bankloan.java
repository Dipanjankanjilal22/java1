//encapsulation methodology
public class bankloan {
private String name;
private int age;
private int amount;
public void setname(String name) {
	this.name=name;
}
public void setage(int age) {
	this.age=age;
}
public void setamount(int amount) {
	this.amount=amount;
}
public String getname() {
	return name;
}
public int getage() {
	return age;
}
public int getamount() {
	return amount;
}
}
